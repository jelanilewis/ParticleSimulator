package particle_simviewer;

/**
 *
 * @author jelanilyles
 */
public class Particle {
    private double posx;
    private double posy;
    private double vel;
    private double theta;
    private double dr;
    private double posx0;
    private double posy0;
    private double dx;
    private double dy;
    private double dxbox;
    private double dybox;
    
    public Particle() {
        
    }

    /**
     * @return the posx
     */
    public double getPosx() {
        return posx;
    }

    /**
     * @param posx the posx to set
     */
    public void setPosx(double posx) {
        this.posx = posx;
    }

    /**
     * @return the posy
     */
    public double getPosy() {
        return posy;
    }

    /**
     * @param posy the posy to set
     */
    public void setPosy(double posy) {
        this.posy = posy;
    }

    /**
     * @return the vel
     */
    public double getVel() {
        return vel;
    }

    /**
     * @param vel the vel to set
     */
    public void setVel(double vel) {
        this.vel = vel;
    }

    /**
     * @return the theta
     */
    public double getTheta() {
        return theta;
    }

    /**
     * @param theta the theta to set
     */
    public void setTheta(double theta) {
        this.theta = theta;
    }

    /**
     * @return the dr
     */
    public double getDr() {
        return dr;
    }

    /**
     * @param dr the dr to set
     */
    public void setDr(double dr) {
        this.dr = dr;
    }

    /**
     * @return the posx0
     */
    public double getPosx0() {
        return posx0;
    }

    /**
     * @param posx0 the posx0 to set
     */
    public void setPosx0(double posx0) {
        this.posx0 = posx0;
    }

    /**
     * @return the posy0
     */
    public double getPosy0() {
        return posy0;
    }

    /**
     * @param posy0 the posy0 to set
     */
    public void setPosy0(double posy0) {
        this.posy0 = posy0;
    }

    /**
     * @return the dx
     */
    public double getDx() {
        return dx;
    }

    /**
     * @param dx the dx to set
     */
    public void setDx(double dx) {
        this.dx = dx;
    }

    /**
     * @return the dy
     */
    public double getDy() {
        return dy;
    }

    /**
     * @param dy the dy to set
     */
    public void setDy(double dy) {
        this.dy = dy;
    }

    /**
     * @return the dxbox
     */
    public double getDxbox() {
        return dxbox;
    }

    /**
     * @param dxbox the dxbox to set
     */
    public void setDxbox(double dxbox) {
        this.dxbox = dxbox;
    }

    /**
     * @return the dybox
     */
    public double getDybox() {
        return dybox;
    }

    /**
     * @param dybox the dybox to set
     */
    public void setDybox(double dybox) {
        this.dybox = dybox;
    }
    
    
}
